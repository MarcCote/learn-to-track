#!/usr/bin/env python3
# -*- coding: utf-8 -*-

import argparse

from learn2track import neurotools
from learn2track.utils import Timer

def build_parser():
    DESCRIPTION = ("Display information about a dataset.")
    p = argparse.ArgumentParser(description=DESCRIPTION)

    # General options (optional)
    p.add_argument('dataset', help='file containing a datasset (as generated by `process_streamlines.py`).')
    return p


def main():
    parser = build_parser()
    args = parser.parse_args()
    print(args)

    with Timer("Loading dataset"):
        tractography_data = neurotools.TractographyData.load(args.dataset)

    print(tractography_data)


if __name__ == "__main__":
    main()
